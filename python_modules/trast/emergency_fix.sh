#!/bin/bash

# Быстрое исправление SSH после WARP
# Запустите этот скрипт если SSH заблокирован

echo "🚨 ЭКСТРЕННОЕ ВОССТАНОВЛЕНИЕ SSH"
echo "================================"

# Отключение WARP
echo "1. Отключаем WARP..."
warp-cli disconnect

# Установка безопасного режима
echo "2. Устанавливаем proxy режим..."
warp-cli mode proxy

# Проверка статуса
echo "3. Проверяем статус..."
warp-cli status

echo ""
echo "✅ Готово! SSH должен работать."
echo "📋 Если SSH все еще не работает:"
echo "   1. Перезагрузите сервер"
echo "   2. Или используйте консоль хостинга"
echo ""
echo "⚠️  ВНИМАНИЕ: Никогда не используйте 'warp-cli set-mode warp' на сервере!"
